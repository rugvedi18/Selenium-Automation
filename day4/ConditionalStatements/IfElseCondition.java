package day4.Conditional;

public class IfElseCondition {
    public static void main(String[] args) {
        // Even or odd number

        int num = 18;

        if (num % 2 == 0) {
            System.out.println(num + " is even");
        } else {
            System.out.println(num + " is odd");
        }
    }
}
