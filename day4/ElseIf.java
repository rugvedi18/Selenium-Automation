package day4;

public class ElseIf {
    public static void main(String[] args) {
        // Positive or negative number
        int num = 5;

        if (num > 0) {
            System.out.println("Number is positive");
        } else if (num < 0) {
            System.out.println("Number is negative");
        } else {
            System.out.println("Number is zero");
        }
    }
}
